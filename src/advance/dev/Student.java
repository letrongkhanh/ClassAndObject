package advance.dev;

public class Student {
	String name;
	int age;
	String address;
	String phoneNumber;
	double averageScore;

	// Constructor
	public Student(String name, int age, String address, String phoneNumber, double averageScore) {
		this.name = name;
		this.age = age;
		this.address = address;
		this.phoneNumber = phoneNumber;
		this.averageScore = averageScore;
	}
}
